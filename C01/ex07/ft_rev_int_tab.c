void	ft_rev_int_tab(int *tab, int size)
{
	int	i;
	int	y;
	int	temp_var;

	i = 0;
	y = size - 1;
	while (i < y)
	{
		temp_var = tab[i];
		tab[i] = tab[y];
		tab[y] = temp_var;
		i++;
		y--;
	}
}
